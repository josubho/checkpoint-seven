function sumaMult(numUno, numDos, numTres, numCuatro) {
    sumaUno = numUno + numDos
    sumaDos = numTres + numCuatro
    multi = sumaUno * sumaDos
    if(multi>50) {
        console.log("¡El número es mayor que 50!")
    } else if(multi<50) {
        console.log("¡El número es menor que 50!")
    }
}

sumaMult(1,1,1,1,)
